package BitMasking;

public class Righ_Left_Shift {
    public static void main(String[] args) {
        int n = 9;
		System.out.println(n << 2);// left shift
		int n1 = -9;
		System.out.println(n1 << 2);// left shift
		int a = 15;
		System.out.println(a >> 2);// right shift
		int a1 = -15;
		System.out.println(a1 >> 2);// right shift
    }
}
